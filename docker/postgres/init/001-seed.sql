-- Schéma minimal pour les tests locaux.
-- Le volume PostgreSQL est éphémère, donc ce script est rejoué à chaque démarrage.

CREATE TABLE IF NOT EXISTS app_user (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username VARCHAR(255) NOT NULL,
  email VARCHAR(255) NOT NULL UNIQUE,
  password VARCHAR(255),
  rating INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS object (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  owned_by_id BIGINT NOT NULL REFERENCES app_user(id),
  status VARCHAR(32) NOT NULL DEFAULT 'AVAILABLE',
  state_of_wear VARCHAR(32) NOT NULL,
  category VARCHAR(32),
  material VARCHAR(32)
);

CREATE TABLE IF NOT EXISTS lending (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  borrowed_by_id BIGINT NOT NULL REFERENCES app_user(id),
  offered_by_id BIGINT NOT NULL REFERENCES app_user(id),
  object_id BIGINT NOT NULL REFERENCES object(id),
  started_at TIMESTAMP,
  ended_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS lending_history (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  borrowed_by_id BIGINT NOT NULL REFERENCES app_user(id),
  offered_by_id BIGINT NOT NULL REFERENCES app_user(id),
  object_id BIGINT NOT NULL REFERENCES object(id),
  started_at TIMESTAMP,
  ended_at TIMESTAMP
);

